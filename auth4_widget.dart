  void _verifyPhoneNumber() async {
    String phoneNumber = _phoneNumberController.text; //日本ナンバー対応のために、finalを省略

    if (phoneNumber.startsWith('090') || phoneNumber.startsWith('080') || phoneNumber.startsWith('070')) {
      phoneNumber = '+81' + phoneNumber.substring(1);
    }//日本ナンバー対応のために、追加

    await _auth.verifyPhoneNumber(
      phoneNumber: phoneNumber,
      verificationCompleted: (PhoneAuthCredential credential) async {
        await _auth.signInWithCredential(credential);
      },
      verificationFailed: (FirebaseAuthException e) {
        ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(content: Text('認証に失敗しました: ${e.message}'))
        );
        print(e);
      },
      codeSent: (String verificationId, int? resendToken) {
        setState(() {
          _verificationId = verificationId;
        });
      },
      codeAutoRetrievalTimeout: (String verificationId) {
        setState(() {
          _verificationId = verificationId;
        });
      },
    );
  }

  void _signInWithPhoneNumber() async {
    final String smsCode = _smsController.text;

    try {
      final PhoneAuthCredential credential = PhoneAuthProvider.credential(
        verificationId: _verificationId!,
        smsCode: smsCode,
      );

      final UserCredential userCredential = await _auth.signInWithCredential(credential);
      print('Successfully signed in with phone number: ${userCredential.user}');

    } catch (e) {
      print('Failed to sign in with phone number: $e');
    }
  }
